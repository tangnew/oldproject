public class Question extends BaseEntity {
	private String fkQuestionTypeId;
	private String answer;

	private String content;
	private String fkAccountId;
	private Integer status;

	public String getFkQuestionTypeId() {
		return fkQuestionTypeId;
	}

	public void setFkQuestionTypeId(String fkQuestionTypeId) {
		this.fkQuestionTypeId = fkQuestionTypeId;
	}

	public String getAnswer() {
		return answer;
	}

	public void setAnswer(String answer) {
		this.answer = answer;
	}

	public String getContent() {
		return content;
	}

	public void setContent(String content) {
		this.content = content;
	}

	public String getFkAccountId() {
		return fkAccountId;
	}

	public void setFkAccountId(String fkAccountId) {
		this.fkAccountId = fkAccountId;
	}

	public Integer getStatus() {
		return status;
	}

	public void setStatus(Integer status) {
		this.status = status;
	}

}
